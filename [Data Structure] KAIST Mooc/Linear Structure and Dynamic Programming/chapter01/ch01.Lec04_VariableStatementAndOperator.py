# Data types
# Numeric data type
# - integer, float,...
# String data type
# - character string
# Collection data type
# - list, tuple, dictionary


def main():
    # 복소수
    numComplex = complex(3, 4)
    print(numComplex)
    # 연산자
    a = 10
    b = 2
    print(a/b)  # 나눗셈
    print(a % b)  # 나머지
    # type casting
    a = 10.0
    print(int(a))  # int로 바꾼다
    print(str(a))  # string으로 바꾼다
    # swapping statment
    a, b = b, a
    print(a, b)
